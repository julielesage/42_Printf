#include "../include/ft_printf.h"
#include <stdio.h>

size_t	ft_strlcat(char *dst, char *src, size_t size)
{
	size_t i;
	size_t len;

	len = 0;
	while (dst[len] && len < size)
		len++;
	i = len;
	while (src[len - i] && len + 1 < size)
	{
		dst[len] = src[len - i];
		len += 1;
	}
	if (i < size)
		dst[len] = '\0';
	printf("result strlcat : %s\n", dst);
	return (i + ft_strlen(src));
}
