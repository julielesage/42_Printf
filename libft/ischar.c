#include "ft_printf.h"

int		ischar(char c, char *set)
{
	int i;

	i = 0;
	if (!c || !set)
		return (-1);
	while (set[i] != '\0')
	{
		if (c == set[i])
			return (1);
		i++;
	}
	return (0);
}
